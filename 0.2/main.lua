r = 0

function love.update(dt)
	r = r + dt
	flash = flash + dt
	
	if flash > 0.8 then
		flash = 0
		display = not display
	end
end

function love.draw()
	love.graphics.setFont(small)
	love.graphics.draw(logo, 400, 280, 0, 1, 1, 128, 64)
	love.graphics.draw(moose, 400, 380 + math.sin(r)*10, 0, 1, 1, 64, 64)
	love.graphics.printf(version_string, 480, 560, 300, "right")
	love.graphics.setFont(big)
	
	if display then
		love.graphics.printf("No Game", 300, 500, 200, "center")
	end
end
