function love.load()
	love.window.setTitle("L\195\150VE " .. love._version .. " (" .. love._version_codename .. ")")

	big = love.graphics.newFont(24)
	small = love.graphics.newFont(12)

	love.graphics.setBackgroundColor(0, 138*MUL, 255*MUL)
	moose = love.graphics.newImage(mini_moose)
	logo = love.graphics.newImage(logo256x128)
