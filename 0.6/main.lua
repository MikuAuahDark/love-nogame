	timers = {}

	Timer = {}
	Timer.__index = Timer

	Timer.spawn = function(self, tick, f)
		local o = {
			passed = 0,
			tick = tick,
			f = f
		}
		setmetatable(o, Timer)
		table.insert(timers, o)
	end

	Timer.update = function(self, dt)
		self.passed = self.passed + dt
		while self.passed > self.tick do
			self.passed = self.passed - self.tick
			self.f()
		end
	end

